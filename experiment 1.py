# Experiment 1: Data Types and Operators in Python

# Data Types
a = 10
b = 3.5
c = "Python"
d = True
e = [1, 2, 3]
f = (4, 5, 6)
g = {"name": "Ali", "age": 20}

print("Data Types:")
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))

# Arithmetic Operators
x = 15
y = 4

print("\nArithmetic Operations")
print("Addition:", x + y)
print("Subtraction:", x - y)
print("Multiplication:", x * y)
print("Division:", x / y)
print("Modulus:", x % y)
print("Exponent:", x ** y)

# Relational Operators
print("\nRelational Operators")
print(x > y)
print(x < y)
print(x == y)
print(x != y)

# Logical Operators
p = True
q = False

print("\nLogical Operators")
print("AND:", p and q)
print("OR:", p or q)
print("NOT:", not p)
